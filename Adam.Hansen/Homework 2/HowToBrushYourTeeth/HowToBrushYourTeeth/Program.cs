using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace HowToBrushYourTeeth
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Items required");
            Console.WriteLine("Liquid Waste Receptacle");
            Console.WriteLine("Toothbrush");
            Console.WriteLine("Toothpaste");
            Console.WriteLine("How to brush your teeth");
            Console.WriteLine("Walk to a liquid waste receptacle");
            Console.WriteLine("Pick up the toothpaste in your hand");
            Console.WriteLine("Unscrew the top of the toothpaste and put the top down");
            Console.WriteLine("Pick up the toothbrush with your free hand");
            Console.WriteLine("Make sure the bristles of the toothbrush are pointed up");
            Console.WriteLine("Apply the toothpaste on top of the bristles on the toothbrush");
            Console.WriteLine("Only apply toothpaste to the length of the bristles");
            Console.WriteLine("Put the toothpaste down");
            Console.WriteLine("Open your mouth and place your toothbrush in your mouth with the toothpaste that is on the toothbrush touching your teeth");
            Console.WriteLine("Move the toothbrush along your teeth in circular motions for two minutes");
            Console.WriteLine("Remove your toothbrush from your mouth and spit the liquid into the receptacle");
            Console.WriteLine("Place the toothbrush down");

            Console.ReadKey();
        }
    }
}
